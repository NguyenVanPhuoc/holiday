var map;
var infoWindows = [];
function initMapService() {
	var myLatLng = {lat: Number(latinit), lng: Number(lnginit)};
	var center = new google.maps.LatLng(myLatLng);
	var mapOptions = {
		zoom: 12,
		center: center
	};
	map = new google.maps.Map(document.getElementById("map-service"), mapOptions);
	for (i = 0; i < markers1.length; i++) {
		addMarker(markers1[i], i);
	}
}
function addMarker(marker, iter) {
	var infowindow = new google.maps.InfoWindow({
		content: ''
	});
	var district = marker.district;
	var title = marker.title;
	var pos = new google.maps.LatLng(marker.lat, marker.lng);
	var content = '<div class="img-map"><span>'+marker.price+'</span><img class="thumb-map" src="'+marker.img+'"></div><h4><a target="_blank" href="'+marker.link+'">'+marker.title+'</a></h4><p class="info"><span><i class="fas fa-map"></i> '+marker.area+'</span><span><i class="fas fa-calendar"></i> '+marker.date+'</span></p>';

	marker1 = new google.maps.Marker({
		title: title,
		position: pos,
		district: district,
		price: marker.price,
		city: marker.city,
		room_type: marker.room_type,
		iter: iter,
		map: map,
		animation: google.maps.Animation.DROP,
	});

	gmarkers1.push(marker1);
	google.maps.event.addListener(infowindow, 'domready', function() {
	    var iwOuter = $('.gm-style-iw');
	    var iwBackground = iwOuter.prev();
	    iwBackground.children(':nth-child(2)').css({'display' : 'none'});
	    iwBackground.children(':nth-child(4)').css({'display' : 'none'});
	    iwOuter.parent().parent().css({padding: '0px'});
	    var iwCloseBtn = iwOuter.next();
	    iwCloseBtn.addClass('close-map');
	});
	google.maps.event.addListener(marker1, 'click', (function (marker1, content) {
		return function () {
			infowindow.setContent(content);
			infowindow.open(map, marker1);
			map.panTo(this.getPosition());
			infoWindows.push(infowindow);
		}
	})(marker1, content));
}
